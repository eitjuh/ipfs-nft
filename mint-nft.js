// Function to upload to IPFS
async function uploadToIPFS(filePath) {
    try {
        const url = `https://api.pinata.cloud/pinning/pinFileToIPFS`;

        // Create a form data instance
        const formData = new FormData();

        // Read the file and append it to form data with the correct name
        const file = fs.readFileSync(filePath);
        formData.append('file', new Blob([file]), 'art.png');

        const response = await axios.post(url, formData, {
            maxBodyLength: "Infinity",
            headers: {
                'Content-Type': `multipart/form-data`,
                'pinata_api_key': PINATA_API_KEY,
                'pinata_secret_api_key': PINATA_SECRET_API_KEY,
            },
        });

        console.log('Upload successful:', response.data);
        return `ipfs://${response.data.IpfsHash}`;
    } catch (error) {
        console.error('Error uploading to IPFS:', error.response ? error.response.data : error);
        throw error;
    }
}

// Function to mint the NFT
async function mintNFT(recipient, metadataURI, providerUrl, privateKey) {
    const provider = new ethers.JsonRpcProvider(providerUrl);
    const wallet = new ethers.Wallet(privateKey, provider);
    const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, wallet);

    const tx = await contract.mintNFT(recipient, metadataURI);
    await tx.wait();
    console.log(`NFT minted with URI: ${metadataURI}`);
}

